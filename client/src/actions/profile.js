// delete account
export const deleteAccount = () => async (dispatch) => {
  if (window.confirm("Are you sure to continue. It can't be undone!")) {
    try {
      await axios.delete("/api/profile");

      dispatch({
        type: CLEAR_PROFILE,
      });

      dispatch({ type: ACCOUNT_DELETED });

      dispatch(
        setAlert(
          "Account deleted sucessfully. We hope to see you soon!",
          "success"
        )
      );
    } catch (err) {
      dispatch({
        type: PROFILE_ERROR,
        payload: { msg: err.response.statusText, status: err.response.status },
      });
    }
  }
};
